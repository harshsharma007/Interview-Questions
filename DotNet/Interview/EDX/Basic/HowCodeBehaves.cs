            09. Code that targets the runtime is known as managed code, while code that does not target the runtime is known as unmanaged code.

            10. The .NET Framework can be hosted by unmanaged components that load the common language runtime into their processes and initiate the execution of managed
                code, thereby creating a software environment that can exploit both managed and unmanaged features.

            11. The .NET Framework not only provides several runtime hosts, but also supports the development of third-party runtime hosts.

            12. For example, ASP.NET hosts the runtime to provide a scalable, server-side environment for managed code.

            13. ASP.NET works directly with the runtime to enable ASP.NET applications and XML Web services, both of which are discussed later in this topic.

            14. All of these components coexist and interact to provide a platform to your C# application.
