using System;
using System.Collections.Generic;
using System.Text;

namespace Interview.EDX.Basic
{
    class DoLoop
    {
        /*
            A do loop, sometimes also referred to as a do while loop, is very similar to a while loop, with the exception that a do loop will always execute the body of the loop at 
            least once. In a while loop, if the condition is false from the start, the body of the loop will never execute.
            
            You might want to use a do loop if you know that the code will only execute in response to a user prompt for data. In this scenario, you know that the application will need 
            to process at least one piece of data, and can therefore use a do loop. The following code example shows the use of a do loop.
            
            do
            {
                // Process the data.
                response = PromptUser();
            } while (response != "Quit")
        */
    }
}
