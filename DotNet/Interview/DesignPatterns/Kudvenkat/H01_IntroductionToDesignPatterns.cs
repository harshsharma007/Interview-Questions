            What are Design Patterns?
            
            - Design Patterns are evolved as reusable solutions to the problems that we encounter every day of programming.
            - They are generally targeted at solving the problems of object generation and integration.
            - In other words, Design Patterns acts as a template which can be applied to the real world programming problems.
            
            Evolution of Design Patterns and GoF
            
            - The four authors of the book "Elements of reusable Object-Oriented software" are referred as Gang of Four. They were the ones who brought the concepts of
              Design Patterns.
            - The book is divided into two parts with first part explaining about the pros and cons of Object Oriented Programming and second part explaining the
              evolution of 23 classic software Design Patterns.
            - From then, Gang of Four Design Patterns has made a significant role in the software development life cycle.
            
            Types of Design Patterns
            There are 3 types of Design Patterns. Gangs of Four have created the Design Patterns into three types based on different problems encountered in the
            real-time applications.
            
            - Creational
              This type deals with the object creation and initialization. This pattern gives the program more flexibility in deciding which objects need to be created
              for a given case.
              Eg: Singleton, Factory, Abstract Factory etc.
            
            - Structural
              This type deals with class and object composition. This pattern focuses on decoupling interface and implementation of classes and its objects.
              Eg: Adapter, Bridge etc.
            
            - Behavioral
              This type deals with the communication between Classes and Objects.
              Eg: Chain of Responsibility, Command, Interpreter etc.
        */
    }
