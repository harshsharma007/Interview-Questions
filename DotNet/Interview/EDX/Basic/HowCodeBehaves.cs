            04. The runtime is responsible for managing your code at execution time.

            05. It provides core services such as memory management, thread management and remoting.

            06. The CLR enforces strict type safety and provides security and robustness for managed applications.

            07. The class library is a comprehensive collection of reusable types .Net classes that you will use to develop applications ranging from traditional
