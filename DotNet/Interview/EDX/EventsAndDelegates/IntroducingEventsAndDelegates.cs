        /*
            Introducing Events and Delegates
            Events are mechanisms that enable objects to notify other objects when something happens. For example, controls on a web page or in a WPF user interface generate events
            when a user interacts with the control, such as by clicking a button. You can create code that subscribes to these events and takes some action in response to an event.
            
            Without events, you code would need to constantly read control values to look for any changes in state that require action. This would be a very inefficient way of
            developing an application. In this lesson, you will learn how to create, or raise, events and how to subscribe to events.
            
            A delegate is a special type that defines a method signature; in other words, the return type and the parameters of a method. As the name suggests, a delegate behaves like
            a representative for methods with matching signatures.
        */
    }
