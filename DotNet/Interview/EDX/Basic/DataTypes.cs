using System;
using System.Collections.Generic;
using System.Text;

namespace Interview.EDX.Basic
{
    class DataTypes
    {
        /*
            All applications store and manipulate data within the computer's memory. C# supports two kinds of data types used to represent real-world information. Value types are 
            so-called because they contain the actual value of the data they store. For example, you might have an int type that stores the value 3. The literal value of 3 is stored in
            the variable that you declare to hold it.
            
            With the exception of DateTime and string, in the table below, the data types listed are aliases for structs in .NET that represent the data types in the Microsoft .NET
            Framework. Anyplace you can use int you can also use System.Int32.
            
            Reference types are also known as objects. Reference types are created from class files. A reference type stores a reference to the location in memory of  the object. If you
            are familiar with C/C++ then you can think of a reference to the memory location to be the same as a pointer. C# does not require you to use pointers.
            
            The following table shows the most commonly used value types.
            
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       Type      |                        Description                            |     Size (bytes)         |     .NET Type      |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       int       |     Whole numbers                                             |         4                |    System.Int32    |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       long      |     Whole numbers (bigger range)                              |         8                |    System.Int64    |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       float     |     Floating-point numbers                                    |         4                |    System.Single   |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       double    |     Double precision (more accurate) floating-point numbers   |         8                |    System.Double   |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       decimal   |     Monetary values                                           |         16               |    System.Decimal  |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       char      |     Single Character                                          |         2                |    System.Char     |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       bool      |     Boolean                                                   |         1                |    System.Boolean  |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       DateTime  |     Moments in time                                           |         8                |    System.DateTime |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
            |       string    |     Sequence of characters                                    |      2 per character     |    System.String   |
            +-----------------+---------------------------------------------------------------+--------------------------+--------------------+
        */
    }
}
