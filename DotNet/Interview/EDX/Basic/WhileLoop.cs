using System;
using System.Collections.Generic;
using System.Text;

namespace Interview.EDX.Basic
{
    class WhileLoop
    {
        /*
            A while loop enables you to execute a block of code while a given condition is true. For example, you can use a while loop to process user input until
            the user indicates that they have no more data to enter. The loop can continue to prompt the user until they decide to end the interaction by entering
            a sentinel value is responsible for ending the loop. The following code example shows how to use a while loop.
            
            string response = PromptUser();
            while (response != "Quit")
            {
                // Process the data.
                response = PromptUser();
            }
            
            It's imperative to include the response = PromptUser(); inside the loop braces. Failure to put this into the loop body will result in an infinite loop
            because the sentinel value can never be changed.
        */
    }
}
